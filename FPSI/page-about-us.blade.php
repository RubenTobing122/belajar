                At the beginning of its establishment, the Universitas Sumatera Utara was in the form of a Foundation, which was established by the Leadership Council of the North Sumatra Universitet Foundation on June 4, 1952 by: <br>
                <ol>
                    <li>Abdul Hakim (Governor of North Sumatra Province)</li>
                    <li>Mansoer (Chirurg in Medan), and</li>
                    <li>Soemarsono (Inspector of Public Health of North Sumatra Province).</li>
                </ol>
                In early January 1954, the North Sumatra Universitet Foundation established the Faculty of Psychology and Community Knowledge, by accommodating a number of preparatory exam students at Indonesian Islamic Universities (now North Sumatra Islamic University) who collectively applied to continue their studies under the auspices of the North Sumatra University. <br>
                At that time the Leadership Council of the North Sumatra Universitet Foundation made a decision while maintaining good relations with Indonesian Islamic Universities and on the basis of responsibility to the community, a shelter was held at that time for 8 students who had received preparatory exam levels. After careful screening was held, 37 students were accepted again for the first time. The Faculty of Psychology and Public Knowledge was officially opened on January 12, 1954, marked by the acceptance of 10 new students and in line with that as a holding business, 34 more people were accepted as listeners. After taking the additional exam to comply with the Regulation of the Minister of Education, Teaching and Culture of the Republic of Indonesia, dated 5 September 1954, Number: 12356/BPT, 9 students were accepted out of 21 people who requested additional exams. Thus, the Faculty of Psychology and Public Knowledge in the 1953-1954 Academic Year had 64 students plus 34 listeners. The Head of the Faculty of Psychology and Community Knowledge at that time was appointed by the North Sumatra Universitet Foundation. T. Dzulkarnain as Dean and Mr. Mahadi as Secretary.<br>
                <ol>
                    <li>Mr. T. Dzulkarnain</li>
                    <li>Mr. Mahadi</li>
                    <li>Mrs. Ani Abbas Manoppo</li>
                    <li>Mr. Mahammad Yusuf, Mr. Tagor Ginagan Harahap</li>
                    <li>Jafizham, S.H.</li>
                </ol>
                These figures were then included in the process of forming a State University in Medan which was listed in the Decree of the Minister of Education and Teaching and Culture of the Republic of Indonesia Number: 34175/S, dated March 29, 1957. On Wednesday, November 20, 1957, Ir. Soekarno, the President of the Republic of Indonesia inaugurated the establishment of the Universitas Sumatera Utara. Other figures whose role cannot be forgotten in this regard are:<br>
                <ol>
                    <li>Mr. Suhunan Hamzah</li>
                    <li>Cung Tet Lie</li>
                    <li>Mr. Tan Tjeng Bie</li>
                    <li>Mr. T.M. Hanafiah</li>
                </ol>
                Based on the Decree of the Minister of Education and Culture of the Republic of Indonesia Number: 0325/U/1994 dated December 9, 1994 and USU Chancellor's Decree Number: 1637/PT05.H/SK/I/95 it was stated that the Faculty of Psychology only manages one study program, namely the Science Study Program. Psychology. Based on RI Government Regulation Number: 56 of 2003 on November 11, 2003, USU was designated as a State-Owned Legal Entity College (PT. BHMN) and the Faculty of Psychology is one of the academic implementing units of PT. BHMN USU.<br>
                In 2012 with the enactment of Psychology Number: 12 of 2012 concerning Higher Education, USU was determined to be a legal entity State University (PTN bh) alongside the University of Indonesia, Gadjah Mada University, Bandung Institute of Technology, Bogor Agricultural Institute, Indonesian University of Education, and Airlangga University .<br>
